def infer_dish_type(name):
    rules = [
        ('fried-rice', r'볶음밥'),
        ('rice-bowl', r'덮밥|주먹밥|오므라이스|오야코|규동|김밥'),
        ('noodle', r'라면|우동|국수|파스타|스파게티|쫄면|비빔면|냉면|잔치면|수제비|마카로니'),
        ('sandwich', r'샌드위치|샌드'),
        ('toast', r'토스트|그릴드치즈|베이컨에그'),
        ('salad', r'샐러드'),
        ('stew', r'찌개|찜|조림|전골|라조기'),
        ('soup', r'국$|미역국|계란국|된장국|콩나물국|북어국|어묵국|김치국|시래기국|육개장|순두부|탕$'),
        ('pancake', r'전$|전[^볶]|부침|튀김|지짐|감자전|김치전|파전|호박전|부추전|치즈전|해물파전|김치전병'),
        ('snack', r'떡볶이|떡구이|라면땅|핫도그|시리얼|떡라면|치즈떡'),
        ('dessert', r'케이크|맛탕|고구마맛탕'),
        ('drink', r'스무디|라떼|오트밀|우유시리얼|바나나우유|고구마라떼'),
        ('stir-fry', r'볶음|볶아|스크램블|무침|볶음$'),
    ]
    for dtype, pattern in rules:
        if __import__('re').search(pattern, name):
            return dtype
    return 'default'


def add(name, cat, mins, diff, cal, ings, steps, dish_type=None):
    RECIPES.append({
        "name": name, "category": cat, "dishType": dish_type or infer_dish_type(name),
        "cookTime": mins, "difficulty": diff,
        "calories": cal, "ingredients": ings, "steps": steps,
    })

